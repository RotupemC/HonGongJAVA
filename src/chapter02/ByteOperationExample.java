package chapter02;

public class ByteOperationExample {
//정수 타입의 연산
	public static void main(String[] args) {
		byte result1 = 10 + 20; //byte타입의 30 - 컴파일 단계에서 연산
		System.out.println(result1);
		
		byte x = 10;
		byte y = 20;
		int result2 = x + y; //int x + int y = int 30
		System.out.println(result2);

	}

}
